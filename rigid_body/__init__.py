from . import rigid_body

classes = (
    rigid_body.OBJECT_OT_make_rigid_body,
)


def register():
    from bpy.utils import register_class
    for cls in classes:
        register_class(cls)


def unregister():
    from bpy.utils import unregister_class
    for cls in reversed(classes):
        unregister_class(cls)
